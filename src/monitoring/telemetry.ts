// ── Full Setup Script ──────────────────────────────────────────

/**
 * Generate a complete setup script that:
 * 1. Creates the OTel collector config
 * 2. Creates the Prometheus config (if Prometheus backend)
 * 3. Creates the Docker Compose file
 * 4. Creates the env vars script
 * 5. Starts the stack and sources the env
 */
export function generateFullSetupScript(config: OtelConfig): string {
  return `#!/bin/bash
set -euo pipefail

# Claude Code Monitoring — Full Setup
# Generated by @jadecli/claude-knowledge-sdk
# Backend: ${config.backend}

echo "Setting up Claude Code monitoring with ${config.backend}..."

# Step 1: Create OTel collector config
mkdir -p monitoring
cat > monitoring/otel-collector-config.yaml << 'OTEL_EOF'
${generateOtelCollectorConfig(config)}OTEL_EOF

${
  config.backend === 'prometheus'
    ? `# Step 2: Create Prometheus config
cat > monitoring/prometheus.yml << 'PROM_EOF'
${generatePrometheusConfig()}PROM_EOF
`
    : '# Step 2: Skipped (not using Prometheus)'
}

# Step 3: Create Docker Compose
cat > monitoring/docker-compose.yml << 'COMPOSE_EOF'
${generateDockerCompose(config.backend)}COMPOSE_EOF

# Step 4: Create env vars script
cat > monitoring/claude-otel-env.sh << 'ENV_EOF'
${generateOtelShellScript(config)}ENV_EOF

echo ""
echo "Setup complete! To start monitoring:"
echo ""
echo "  cd monitoring && docker compose up -d"
echo "  source claude-otel-env.sh"
echo "  claude  # OTel telemetry is now enabled"
echo ""
${
  config.backend === 'prometheus'
    ? `echo "Dashboards:"
echo "  Prometheus: http://localhost:9090"
echo "  Grafana:    http://localhost:3000 (admin/admin)"
echo ""`
    : ''
}
echo "Key metrics to query:"
echo "  ${OTEL_METRICS.cost}         — total cost in USD"
echo "  ${OTEL_METRICS.tokens}  — token usage by type"
echo "  ${OTEL_METRICS.sessions}    — session count"
`;
}

